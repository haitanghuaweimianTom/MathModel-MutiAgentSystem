def get_prompt(prompt_type: str, **kwargs) -> str:
    """
    获取填充后的提示词

    Args:
        prompt_type: 提示词类型
        **kwargs: 填充参数

    Returns:
        填充后的提示词字符串
    """
    if prompt_type in USER_PROMPTS:
        template = USER_PROMPTS[prompt_type]
        try:
            return template.format(**kwargs)
        except KeyError as e:
            return f"提示词模板缺少参数: {e}"
    else:
        return f"未知的提示词类型: {prompt_type}"


def get_system_prompt(agent_type: str) -> str:
    """
    获取Agent的系统提示词

    Args:
        agent_type: Agent类型

    Returns:
        系统提示词字符串
    """
    return SYSTEM_PROMPTS.get(agent_type, "你是一个通用的AI助手。")
